/**
 * Единая внутренняя модель документа (ТЗ ч.3, п.12).
 * Все конвертеры приводят вход к NormalizedDoc, дальше из неё собирается
 * чистый EPUB, который читалка (foliate) открывает как перетекаемый текст —
 * со всеми настройками шрифта/интервала/тем.
 */
import type { BookFormat } from '@reader/core';

/** Метаданные нормализованной книги. */
export interface ConvMeta {
  title?: string;
  author?: string;
  /** ISO-код языка (по умолчанию 'ru'). */
  language?: string;
}

/** Глава книги: заголовок + валидный XHTML-фрагмент содержимого. */
export interface Chapter {
  /** Идентификатор файла главы в EPUB (без расширения). */
  id: string;
  /** Заголовок для оглавления. */
  title?: string;
  /**
   * Содержимое главы как валидный XHTML (то, что попадёт внутрь <body>).
   * Конвертер обязан выдавать корректную разметку.
   */
  html: string;
}

/** Нормализованный документ — общий промежуточный формат. */
export interface NormalizedDoc {
  metadata: ConvMeta;
  chapters: Chapter[];
}

/**
 * Адаптер-конвертер одного семейства форматов в NormalizedDoc.
 * Реализации регистрируются в реестре (registry.ts) и взаимозаменяемы:
 * пользователь без тяжёлого WASM всё равно получает базовый функционал.
 */
export interface Converter {
  /** Имя для логов/диагностики. */
  name: string;
  /** Форматы, которые умеет конвертировать. */
  formats: BookFormat[];
  /** Преобразовать файл в единую модель документа. */
  convert(file: File): Promise<NormalizedDoc>;
}
