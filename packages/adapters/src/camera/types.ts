/**
 * Платформенный интерфейс доступа к камере / выбору изображений.
 * UI и book-scanner зависят только от этого интерфейса, не зная платформы.
 * Web-реализация — здесь же; нативные (Tauri mobile) добавляются в Фазе 1
 * без изменения вызывающего кода.
 */

export interface CaptureOptions {
  /** Разрешить выбор нескольких изображений за раз (загрузка из галереи). */
  multiple?: boolean;
  /** Предпочесть тыловую камеру (для съёмки страниц). */
  facing?: 'environment' | 'user';
}

export interface CameraAdapter {
  /** Доступна ли камера/съёмка в текущем окружении. */
  isAvailable(): boolean;
  /**
   * Снять/выбрать одно или несколько изображений.
   * Возвращает файлы изображений (image/*). Пустой массив — отмена.
   */
  capture(options?: CaptureOptions): Promise<File[]>;
}
